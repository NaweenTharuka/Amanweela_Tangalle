package entity;

public class Salary {
	
	private Integer id;
	private Integer staffid;
	private String month;
	private String bsalary; 
	private String transport;
	private String allowance;
	private String ot;
	private Integer totsalary;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getStaffid() {
		return staffid;
	}
	public void setStaffid(Integer staffid) {
		this.staffid = staffid;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getBsalary() {
		return bsalary;
	}
	public void setBsalary(String bsalary) {
		this.bsalary = bsalary;
	}
	public String getTransport() {
		return transport;
	}
	public void setTransport(String transport) {
		this.transport = transport;
	}
	public String getAllowance() {
		return allowance;
	}
	public void setAllowance(String allowance) {
		this.allowance = allowance;
	}
	public String getOt() {
		return ot;
	}
	public void setOt(String ot) {
		this.ot = ot;
	}
	public Integer getTotsalary() {
		return totsalary;
	}
	public void setTotsalary(Integer totsalary) {
		this.totsalary = totsalary;
	}
	
	
	
	

	
	

}
